celcius = 30
# Temperatura w stopniach Fahrenheita (°F) = (Temperatura w stopniach Celsjusza (°C) * 9/5) + 32
fahrenheit = (celcius*9/5)+32

# Temperatura w kelwinach = Temperatura w stopniach celciusza + 273,15
kelvin = celcius+273.15

print(f"Temperatura w stopniach celciusza: {celcius}, w fahrenheitach {fahrenheit}, w kelvinach: {kelvin}")